""" VARIABLES
-------------------------- """
"""  Python is a "type-inferred" language :
 You don't have to explicitly define the variable type (let or const)! """



# DECLARE AND ASSIGN VALUE
website = "apple.com"
print(website)

website = "powercoders.org" # You can replace the value. Python is very flexible language.
print(website)
# We don't use ; in python.


# ASSIGNING MULTIPLE VALUES TO MULTIPLE VARIABLES
a, b, c = 5, 3.2, "Hello!"
print(a)
print(b)
print(c)


# ASSIGN SAME VALUE FOR MULTIPLE VARIABLES
a = b = c = 8
c = 5 # You can change just one variable like that.
print(a)
print(b)
print(c)


